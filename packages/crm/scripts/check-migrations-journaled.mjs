#!/usr/bin/env node
// packages/crm/scripts/check-migrations-journaled.mjs
//
// 2026-05-29 — Guard against "root cause A": an agent writes a drizzle/*.sql
// file but forgets to register it in meta/_journal.json. `drizzle-kit migrate`
// ONLY applies entries listed in the journal (see drizzle-orm's migrator.js:
// it iterates `journal.entries`, never the directory), so an un-journaled .sql
// file is invisible — it silently never runs. Both production outages
// (0019_silky_viper / users.stripe_payment_method_id, and
// 0055_users_onboarding_completed_at) were exactly this.
//
// This check runs in CI and in the build chain BEFORE deploy, so an orphaned
// migration fails the PR instead of 500ing authenticated requests in prod.
//
// ── Why this repo needs a baseline, not a naive "every file must be journaled"
//
// SeldonFrame's drizzle/ directory is a HYBRID. There are two classes of file:
//
//   1. Drizzle-managed migrations — generated by `drizzle-kit generate`, each
//      with a meta/NNNN_snapshot.json, applied by `drizzle-kit migrate`. These
//      MUST be in the journal. (e.g. 0000_panoramic_jubilee, 0018_short_spiral,
//      0019_silky_viper.) A handful of hand-authored files were also added to
//      the journal so migrate applies them (0014–0018, 0045–0048, 0054, 0055).
//
//   2. Out-of-band migrations — hand-authored .sql applied DIRECTLY to prod via
//      the Neon MCP / scripts/apply-prod-migrations.mjs, which does NOT write to
//      __drizzle_migrations and whose files are deliberately NOT journaled so
//      `drizzle-kit migrate` won't try to re-run them (their tables already
//      exist). There are 44 such files today (0001_soul_package_tracking …
//      0053_proposal_editable_proposal_copy). They are intentional, not bugs.
//
// A blanket "every .sql must be journaled" rule would flag all 44 out-of-band
// files as orphans on day one — the check would be noise and get disabled. So
// we freeze those 44 as a documented BASELINE. The check then fails only on a
// NEW .sql file that is neither journaled nor in the baseline — which is
// precisely the 0019 / 0055 failure mode.
//
// ── Maintenance
//
// When you add a migration:
//   • Generated via `drizzle-kit generate` → it's journaled automatically; do
//     nothing here.
//   • Hand-authored to run via `drizzle-kit migrate` → add a journal entry (the
//     fix in commit d1b86b33 is the template). Do NOT add it to the baseline.
//   • Hand-authored + applied out-of-band via Neon MCP (NOT through
//     drizzle-kit migrate) → add its tag to OUT_OF_BAND_BASELINE below with a
//     one-line note. This is the rare case; prefer the journaled path.
//
// The baseline should only ever shrink (as out-of-band files are folded into
// the journal) or grow by deliberate, reviewed addition. It must never grow
// silently — that's the whole point.

